/* ================= ADD PLANE LAYER ================= */

export function addRoadPlaneLayer(map) {
  if (!map.getSource('road-planes')) {
    map.addSource('road-planes', {
      type: 'geojson',
      data: roadPlanes
    });
  }

  if (!map.getLayer('road-plane-fill')) {
    map.addLayer({
      id: 'road-plane-fill',
      type: 'fill',
      source: 'road-planes',
      paint: {
        'fill-color': '#b8f18c',
        'fill-opacity': 1
      }
    });
  }

  if (!map.getLayer('road-plane-outline')) {
    map.addLayer({
      id: 'road-plane-outline',
      type: 'line',
      source: 'road-planes',
      paint: {
        'line-color': '#9a9a9a',
        'line-width': 2
      }
    });
  }
}
